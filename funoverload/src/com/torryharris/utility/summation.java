package com.torryharris.utility;

public class summation {
    public long add(int a, int b){
        return (a+b);

    }
    public long add(int a, float b){
        return (float) (a+b);
    }
    public long add(int a, float b, int c){
        return (long) (a+b+c);
    }
    public long add(int a, int b, int c){
        return (a+b+c);
    }
}
